namespace Tygh\Enum\Addons\Ab_antibot;

class Modes
{
const DO_NOT_HIDE = 'N';
const HIDE_EXCEPT_WHITE_LIST = 'W';
const HIDE = 'A';
public static function getList(array $exclude = [])
{
$modes = [
self::DO_NOT_HIDE => self::DO_NOT_HIDE,
self::HIDE_EXCEPT_WHITE_LIST => self::HIDE_EXCEPT_WHITE_LIST,
self::HIDE => self::HIDE,
];
return array_diff($modes, $exclude);
}

public static function getAll()
{
return self::getList();
}
}
