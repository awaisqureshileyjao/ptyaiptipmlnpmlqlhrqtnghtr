namespace Tygh\Enum\Addons\Ab_antibot;

class BotTypes
{
const NOT_BOT = 'N';
const BLACK = 'B';
const WHITE = 'W';
public static function getList(array $exclude = [])
{
$modes = [
self::NOT_BOT => self::NOT_BOT,
self::BLACK => self::BLACK,
self::WHITE => self::WHITE,
];
return array_diff($modes, $exclude);
}
public static function getOnlyBots()
{
return self::getList([self::NOT_BOT]);
}

public static function getAll()
{
return self::getList();
}
}
